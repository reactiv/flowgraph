interface ViewCardProps {
  view: ViewTemplate | { id: 'list' | 'schema'; name: string; description?: string };
  isSelected: boolean;
  isBuiltIn?: boolean;
  onSelect: () => void;
  onEdit?: () => void;
  onDelete?: () => void;
}

export function ViewCard({
  view,
  isSelected,
  isBuiltIn = false,
  onSelect,
  onEdit,
  onDelete,
}: ViewCardProps) {
  const [showMenu, setShowMenu] = useState(false);

  // Determine which icon to use based on view style
  const getIcon = () => {
    if (view.id === 'list') {
      return <ListIcon className="h-5 w-5" />;
    }
    if (view.id === 'schema') {
      return <GraphIcon className="h-5 w-5" />;
    }

    // Get the style from the first level config
    if ('levels' in view && view.levels) {
      const firstLevel = Object.values(view.levels)[0];
      if (firstLevel) {
        switch (firstLevel.style) {
          case 'kanban':
            return <KanbanIcon className="h-5 w-5" />;
          case 'tree':
            return <TreeIcon className="h-5 w-5" />;
          case 'timeline':
            return <TimelineIcon className="h-5 w-5" />;
          case 'table':
            return <TableIcon className="h-5 w-5" />;
          case 'cards':
            return <CardsIcon className="h-5 w-5" />;
        }
      }
    }

    // Default to kanban
    return <KanbanIcon className="h-5 w-5" />;
  };

  return (
    <div
      className={`relative flex shrink-0 w-[160px] cursor-pointer flex-col rounded-lg border-2 p-3 transition-all ${
        isSelected
          ? 'border-blue-500 bg-blue-50 shadow-md'
          : 'border-gray-200 bg-white hover:border-gray-300 hover:shadow-sm'
      }`}
      onClick={onSelect}
      onMouseEnter={() => !isBuiltIn && setShowMenu(true)}
      onMouseLeave={() => setShowMenu(false)}
    >
      {/* Icon and name */}
      <div className="flex items-center gap-2">
        <span className={isSelected ? 'text-blue-600' : 'text-gray-500'}>
          {getIcon()}
        </span>
        <span className={`truncate text-sm font-medium ${isSelected ? 'text-blue-900' : 'text-gray-900'}`}>
          {view.name}
        </span>
      </div>

      {/* Description */}
      {view.description && (
        <p className={`mt-1.5 line-clamp-2 text-xs leading-tight ${isSelected ? 'text-blue-700' : 'text-gray-500'}`}>
          {view.description}
        </p>
      )}

      {/* Actions menu */}
      {showMenu && !isBuiltIn && (onEdit || onDelete) && (
        <div className="absolute right-1 top-1 flex gap-0.5" onClick={(e) => e.stopPropagation()}>
          {onEdit && (
            <button
              onClick={onEdit}
              className="rounded p-1 text-gray-400 hover:bg-gray-100 hover:text-gray-600"
              title="Edit view"
            >
              <PencilIcon className="h-3.5 w-3.5" />
            </button>
          )}
          {onDelete && (
            <button
              onClick={onDelete}
              className="rounded p-1 text-gray-400 hover:bg-red-50 hover:text-red-600"
              title="Delete view"
            >
              <TrashIcon className="h-3.5 w-3.5" />
            </button>
          )}
        </div>
      )}
    </div>
  );
}

